#include "../include/libft.h"

void	ft_lstclear(t_list **lst, void (*del) (void*))
{
	t_list	*current;

	if (!lst || !*lst)
		return ;
	while (*lst)
	{
		current = *lst;
		*lst = current->next;
		ft_lstdelone(current, del);
	}
}
